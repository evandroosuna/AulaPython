// Variáveis
let NumeroInputado;

// Iniciar variáveis
NumeroInputado = 0;

// Inserir NumeroInputado
NumeroInputado = 10;

// Processamento
if (NumeroInputado > 0) {
    console.log("O número é positivo");
} else if (NumeroInputado < 0) {
    console.log("O número é negativo");
} else {
    console.log("O número é zero");
}